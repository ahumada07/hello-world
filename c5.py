import pickle
from urllib.request import urlopen

with urlopen("http://www.pythonchallenge.com/pc/def/banner.p") as response:
	body = response.read()

result = pickle.loads(body)
banner =''
for x in result:
	banner+='\n'
	for y in x:
		banner+=(y[0]*y[1])
print(banner)

